package org.cookie.copy;

public class Stu2 {
    private String s3;
    private String s4;
    private String ss;

    public String getSs() {
        return ss;
    }

    public void setSs(String ss) {
        this.ss = ss;
    }

    public String getS3() {
        return s3;
    }

    public void setS3(String s3) {
        this.s3 = s3;
    }

    public String getS4() {
        return s4;
    }

    public void setS4(String s4) {
        this.s4 = s4;
    }

    @Override
    public String toString() {
        return "Stu2{" +
                "s3='" + s3 + '\'' +
                ", s4='" + s4 + '\'' +
                '}';
    }
}
